export function typesTheme(type: string) {
    if (type === 'normal') return '#B0C4DE'

    if (type === 'poison') return '#9932CC'

    if (type === 'bug') return '#7FFF00'

    if (type === 'flying') return '#E0FFFF'

    if (type === 'water') return '#1E90FF'

    if (type === 'grass') return '#00FF00'

    if (type === 'fire') return '#FF0000'

    if (type === 'electric') return '#FFFF00'

    if (type === 'ground') return '#D2B48C'

    if (type === 'fairy') return '#DDA0DD'

    if (type === 'fighting') return '#D2691E'

    if (type === 'psychic') return '#8B008B'

    if (type === 'ghost') return '#8B008B'

    if (type === 'dragon') return '#191970'

    if (type === 'rock') return '#D2B48C'

    if (type === 'ice') return '#87CEEB'


    return '#B0C4DE'
}